Getal1 = float(input("Voer je eerste getal in: "))
optie = input ("Welke optie wil je uitvoeren")
Getal2 = float(input("Voer je tweede getal in: "))

if optie == "+":
    print("resultaat", Getal1 + Getal2)

elif optie == "-":
    print("resultaat", Getal1 - Getal2)

elif optie == "x":
    print("resultaat", Getal1 * Getal2)

elif optie == ":":
    print("resultaat", Getal1 / Getal2)